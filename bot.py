async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    _cancel_start_cleanup(context)
    context.user_data.clear()
    user = update.effective_user
    record_user(user.id, user.username, user.full_name)
    chat_id = update.effective_chat.id
    msg1 = await update.message.reply_text(
        "Bot is working 🔥", reply_markup=main_keyboard
    )
    msg2 = await update.message.reply_text(DISCLAIMER)
    msg_ids = [update.message.message_id, msg1.message_id, msg2.message_id]
    if context.job_queue is not None:
        job = context.job_queue.run_once(
            _delete_start_messages,
            when=7 * 60 * 60,
            data={"chat_id": chat_id, "msg_ids": msg_ids},
            name=f"start_cleanup_{chat_id}",
        )
        context.user_data["start_cleanup_job"] = job


async def stats(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user = update.effective_user
    if ADMIN_ID is not None and user.id != ADMIN_ID:
        await update.message.reply_text("⛔ This command is for the admin only.")
        return

    s = query_global_stats()
    total = s["total"]
    correct = s["correct"]
    wrong = s["wrong"]
    rate = f"{correct / total * 100:.1f}%" if total else "N/A"

    lines = [
        "📊 *Global Stats*",
        "━━━━━━━━━━━━━━",
        f"👥 Total users:       *{s['users']}*",
        f"🔢 Total predictions: *{total}*",
        f"✅ Correct:           *{correct}*",
        f"❌ Wrong:             *{wrong}*",
        f"🎯 Win rate:          *{rate}*",
    ]

    if s["dur"]:
        lines.append("\n📌 *By duration:*")
        for dur, wins, cnt in s["dur"]:
            dr = f"{wins / cnt * 100:.1f}%" if cnt else "N/A"
            lines.append(f"  • {dur or '?'}: {wins}/{cnt} ({dr})")

    if s["top"]:
        lines.append("\n🏆 *Top 5 users:*")
        medals = ["🥇", "🥈", "🥉", "4️⃣", "5️⃣"]
        for i, (uid, uname, fname, wins, cnt) in enumerate(s["top"]):
            display = f"@{uname}" if uname else fname or str(uid)
            dr = f"{wins / cnt * 100:.1f}%" if cnt else "N/A"
            lines.append(f"  {medals[i]} {display}: {wins}/{cnt} ({dr})")

    await update.message.reply_text("\n".join(lines), parse_mode="Markdown")


async def broadcast(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user = update.effective_user
    if ADMIN_ID is not None and user.id != ADMIN_ID:
        await update.message.reply_text("⛔ This command is for the admin only.")
        return

    if not context.args:
        await update.message.reply_text(
            "Usage: /broadcast <message>\n\nExample:\n/broadcast 🔥 New update! Try the latest predictions now."
        )
        return

    text = " ".join(context.args)
    users = get_all_users()

    if not users:
        await update.message.reply_text("No users in the database yet.")
        return

    status_msg = await update.message.reply_text(
        f"📤 Broadcasting to {len(users)} user(s)…"
    )

    sent = 0
    failed = 0
    for uid, uname, fname in users:
        try:
            await context.bot.send_message(chat_id=uid, text=text)
            sent += 1
        except Exception as e:
            logging.warning("Broadcast failed for %s: %s", uid, e)
            failed += 1
        await asyncio.sleep(0.05)

    await context.bot.edit_message_text(
        chat_id=update.effective_chat.id,
        message_id=status_msg.message_id,
        text=(
            f"✅ Broadcast complete\n"
            f"━━━━━━━━━━━━━━\n"
            f"📨 Sent:   *{sent}*\n"
            f"❌ Failed: *{failed}*\n"
            f"👥 Total:  *{len(users)}*"
        ),
        parse_mode="Markdown",
    )


