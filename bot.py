# ── Database ────────────────────────────────────────────────────────────────

DB_PATH = "wingo_stats.db"
_db_lock = threading.Lock()


def init_db() -> None:
    with _db_lock:
        con = sqlite3.connect(DB_PATH)
        con.execute("""
            CREATE TABLE IF NOT EXISTS predictions (
                id          INTEGER PRIMARY KEY AUTOINCREMENT,
                user_id     INTEGER NOT NULL,
                username    TEXT,
                full_name   TEXT,
                duration    TEXT,
                result      TEXT NOT NULL,   -- 'correct' or 'wrong'
                created_at  TEXT NOT NULL
            )
        """)
        con.execute("""
            CREATE TABLE IF NOT EXISTS users (
                user_id    INTEGER PRIMARY KEY,
                username   TEXT,
                full_name  TEXT,
                joined_at  TEXT NOT NULL
            )
        """)
        con.commit()
        con.close()
    logging.info("Database initialised at %s", DB_PATH)


def record_user(user_id: int, username: str | None, full_name: str) -> None:
    with _db_lock:
        con = sqlite3.connect(DB_PATH)
        con.execute(
            "INSERT OR IGNORE INTO users (user_id, username, full_name, joined_at) "
            "VALUES (?, ?, ?, ?)",
            (user_id, username, full_name,
             datetime.utcnow().isoformat(timespec="seconds")),
        )
        con.execute(
            "UPDATE users SET username=?, full_name=? WHERE user_id=?",
            (username, full_name, user_id),
        )
        con.commit()
        con.close()


def get_all_users() -> list[tuple[int, str | None, str]]:
    with _db_lock:
        con = sqlite3.connect(DB_PATH)
        rows = con.execute(
            "SELECT user_id, username, full_name FROM users"
        ).fetchall()
        con.close()
    return rows


def record_feedback(user_id: int, username: str | None, full_name: str,
                    duration: str, result: str) -> None:
    with _db_lock:
        con = sqlite3.connect(DB_PATH)
        con.execute(
            "INSERT INTO predictions (user_id, username, full_name, duration, result, created_at) "
            "VALUES (?, ?, ?, ?, ?, ?)",
            (user_id, username, full_name, duration, result,
             datetime.utcnow().isoformat(timespec="seconds")),
        )
        con.commit()
        con.close()


def query_global_stats() -> dict:
    with _db_lock:
        con = sqlite3.connect(DB_PATH)
        row = con.execute(
            "SELECT COUNT(*), "
            "SUM(CASE WHEN result='correct' THEN 1 ELSE 0 END), "
            "SUM(CASE WHEN result='wrong'   THEN 1 ELSE 0 END), "
            "COUNT(DISTINCT user_id) "
            "FROM predictions"
        ).fetchone()
        top = con.execute(
            "SELECT user_id, username, full_name, "
            "SUM(CASE WHEN result='correct' THEN 1 ELSE 0 END) AS wins, "
            "COUNT(*) AS total "
            "FROM predictions "
            "GROUP BY user_id "
            "ORDER BY wins DESC "
            "LIMIT 5"
        ).fetchall()
        dur = con.execute(
            "SELECT duration, "
            "SUM(CASE WHEN result='correct' THEN 1 ELSE 0 END), "
            "COUNT(*) "
            "FROM predictions "
            "GROUP BY duration"
        ).fetchall()
        con.close()
    total, correct, wrong, users = row
    return {
        "total": total or 0,
        "correct": correct or 0,
        "wrong": wrong or 0,
        "users": users or 0,
        "top": top,
        "dur": dur,
    }


def query_user_stats(user_id: int) -> dict:
    with _db_lock:
        con = sqlite3.connect(DB_PATH)
        row = con.execute(
            "SELECT COUNT(*), "
            "SUM(CASE WHEN result='correct' THEN 1 ELSE 0 END), "
            "SUM(CASE WHEN result='wrong'   THEN 1 ELSE 0 END) "
            "FROM predictions WHERE user_id=?",
            (user_id,),
        ).fetchone()
        streak_rows = con.execute(
            "SELECT result FROM predictions WHERE user_id=? "
            "ORDER BY id DESC LIMIT 10",
            (user_id,),
        ).fetchall()
        con.close()
    total, correct, wrong = row
    streak = 0
    if streak_rows:
        first = streak_rows[0][0]
        for r in streak_rows:
            if r[0] == first:
                streak += 1
            else:
                break
    return {
        "total": total or 0,
        "correct": correct or 0,
        "wrong": wrong or 0,
        "streak": streak,
        "streak_type": streak_rows[0][0] if streak_rows else None,
    }


# ── Helpers ─────────────────────────────────────────────────────────────────

async def _delete_start_messages(context: ContextTypes.DEFAULT_TYPE):
    data = context.job.data
    chat_id = data["chat_id"]
    for mid in data["msg_ids"]:
        try:
            await context.bot.delete_message(chat_id=chat_id, message_id=mid)
        except Exception as e:
            logging.debug("Could not auto-delete %s: %s", mid, e)


def _cancel_start_cleanup(context: ContextTypes.DEFAULT_TYPE):
    job = context.user_data.pop("start_cleanup_job", None)
    if job is not None:
        try:
            job.schedule_removal()
        except Exception:
            pass


